    private void tbanalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tbanalizarActionPerformed
        // TODO add your handling code here:
        File archivo = new File("archivo.txt");
        PrintWriter texto;
        
        try {
            texto = new PrintWriter(archivo);
            texto.print(tbtexto.getText());
            texto.close();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ventanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        try {
            Reader lector = new BufferedReader(new FileReader("archivo.txt"));
            Lexer lexer = new Lexer(lector);
            String resultado = "";
            while (true) {                
                Tokens tokens = lexer.yylex();
                if (tokens == null)
                {resultado +=" FIN";
                tbresult.setText(resultado);
                return;
                }
                switch (tokens) {
                    case ERROR -> resultado += "Simbolo no definodo\n";
                    case Identificador, Numero -> resultado += lexer.lexeme + " Es un " + tokens + "\n";
                    case Reservadas -> resultado += lexer.lexeme + " Palabras " + tokens + "\n";
                    default -> resultado += " Token: " + tokens + "\n";
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(ventanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(ventanaPrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_tbanalizarActionPerformed

